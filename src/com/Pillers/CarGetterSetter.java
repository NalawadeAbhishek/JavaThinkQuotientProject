package com.Pillers;

public class CarGetterSetter {

		private int model;
		private int price;
		private String name;
		private int speed;
		
		public void setModel(int model)
		{
			this.model=model;
		}
		public void setPrice(int price)
		{
			this.price=price;
		}
		public void setName(String name)
		{
			this.name=name;
		}
		public void setSpeed(int speed)
		{
			this.speed=speed;
		}
		public int getModel()
		{
			return model;
		}
		public int getPrice()
		{
			return price;
		}
		public String getName()
		{
			return name;
		}
		public int getSpeed()
		{
			return speed;
		}

}

package com.pack1;

public class Myclass {
	
	private int a=10;
	int b=20;
	protected int c=30;
	public int d=40;

	
	public static void main(String[] args) {
		Myclass m=new Myclass();
		System.out.println(m.a);
		System.out.println(m.b);
		System.out.println(m.c);
		System.out.println(m.d);

	}


	
}

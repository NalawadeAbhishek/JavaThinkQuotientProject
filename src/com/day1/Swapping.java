package com.day1;

public class Swapping {

	public static void main(String[] args) {
		
		int x,y,temp;
		x=10;
		y=20;
		
		System.out.println("before swap x ="+x);
		System.out.println("before swap y ="+y);
		temp=x;
		x=y;
		y=temp;
		
		System.out.println("after swap x ="+x);
		System.out.println("after swap y ="+y);

	}

}

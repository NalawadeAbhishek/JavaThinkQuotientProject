package loop_and_switch_assignment;

public class Odd_Number {

	public static void main(String[] args) {
		//print odd no.betn 521 to 229 using while loop
		int i=521;
		
		while(i>=229)
		{
			if(i%2!=0)
			{
				System.out.print(i+" ");
				
				
			}
			i--;
		}
		
		
	}

}
